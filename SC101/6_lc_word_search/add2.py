import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    lst1 = []
    cur1 = l1
    while cur1 is not None:  # convert l1 ListNode val to list
        lst1.append(cur1.val)  # data type: int
        cur1 = cur1.next
    lst1.reverse()
    num1 = int("".join(map(str, lst1)))  # combine ele in lst1 and convert to num1(int)

    lst2 = []
    cur2 = l2
    while cur2 is not None:  # convert l2 ListNode val to list
        lst2.append(cur2.val)
        cur2 = cur2.next
    lst2.reverse()
    num2 = int("".join(map(str, lst2)))

    num_sum = num1 + num2

    # reverse num_sum
    lst_num = list(str(num_sum))
    lst_num.reverse()

    linked_list = ListNode(int(lst_num[0]), None)  # 1st ListNode of ans
    cur = linked_list
    for i in range(len(lst_num)-1):
        new_node = ListNode(int(lst_num[i+1]), None)
        cur.next = new_node
        cur = cur.next

    return linked_list


